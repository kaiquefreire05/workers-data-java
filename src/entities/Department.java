package entities;

public class Department {

	private String name;

	// construtores

	public Department() {
	}

	public Department(String name) {
		super();
		this.name = name;
	}

	// getter e setters

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}